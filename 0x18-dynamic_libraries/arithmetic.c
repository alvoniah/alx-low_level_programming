/**
 * add - adds two integer
 * @a: first integer
 * @b: second integer
 * Return: sum of integers
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - subtracts one integer from the other
 * @a: first integer
 * @b: second integer
 * Return: difference of integers
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul - multiplies two integers
 * @a: first integer
 * @b: second integer
 * Return: multiple of two integers
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * div - divides two integer
 * @a: first integer
 * @b: second integer
 * Return: result of division
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - determines the modulus of two integers
 * @a: first integer
 * @b: second integer
 * Return: result of modulus
 */
int mod(int a, int b)
{
	return (a % b);
}

